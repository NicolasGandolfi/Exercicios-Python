str=input('Digite algo:')

print('sua string é',str)
print('tipo primitivo desse valor:',type(str))
print('só espaços?:',str.isspace())
print('é numérico?:',str.isnumeric())
print('são numeros?:',str.isdigit()) #somente números
print('maiúsculas?:',str.isupper())
print('minúsculas?:',str.islower())
print('está captalizadas?:',str.istitle())
print('é ASCII?:',str.isascii())
print('é string?:',str.isprintable())
print('é alfabético?:',str.isalpha())



